// scripts/bundle-plugin.mjs
import { build } from "esbuild";
import { cp, mkdir, readdir, rm, stat } from "node:fs/promises";
import { execFileSync } from "node:child_process";
import { homedir } from "node:os";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(fileURLToPath(import.meta.url), "../..");
const BUNDLE_DIR = resolve(ROOT, "plugin/bundle");

async function main() {
  await rm(BUNDLE_DIR, { recursive: true, force: true });
  await mkdir(BUNDLE_DIR, { recursive: true });

  // 1. MCP server entry — bundled, externalizing native/WASM deps
  await build({
    entryPoints: { "mcp-server": resolve(ROOT, "src/index.ts") },
    outdir: BUNDLE_DIR,
    outExtension: { ".js": ".mjs" },
    bundle: true,
    platform: "node",
    format: "esm",
    target: "node20",
    sourcemap: "linked",
    // web-tree-sitter uses Emscripten locateFile() to find .wasm,
    // tree-sitter-wasms ships the grammars — both MUST stay external so
    // the runtime `createRequire(import.meta.url).resolve(...)` finds
    // them in plugin/node_modules/ at runtime.
    // We also externalize pino, fastify and @modelcontextprotocol/sdk due to dynamic require/ESM issues.
    external: [
      "web-tree-sitter",
      "tree-sitter-wasms",
      "pino",
      "fastify",
      "@fastify/static",
      "@modelcontextprotocol/sdk",
      "picomatch",
    ],
    banner: {
      js: "// Generated by scripts/bundle-plugin.mjs — DO NOT EDIT",
    },
  });

  // 2. TDR engine — bundled standalone so hooks/lib/quality-gate.mjs
  //    has a single entry to import classifyTdr() from.
  await build({
    entryPoints: { "tdr-engine": resolve(ROOT, "src/metrics/tdr.ts") },
    outdir: BUNDLE_DIR,
    outExtension: { ".js": ".mjs" },
    bundle: true,
    platform: "node",
    format: "esm",
    target: "node20",
    sourcemap: "linked",
    // TDR module is self-contained today (no external deps beyond Node
    // builtins). Keep this list empty but explicit so a future import
    // that pulls in a heavy dep is caught by the bundle size diff in CI.
    external: [],
    banner: {
      js: "// Generated by scripts/bundle-plugin.mjs — DO NOT EDIT",
    },
  });

  // 3. Copy dashboard static assets into the bundle so resolvePublicRoot()
  //    can find them relative to the bundle entry.
  await cp(
    resolve(ROOT, "src/dashboard/public"),
    resolve(BUNDLE_DIR, "dashboard/public"),
    { recursive: true },
  );

  // 4. Copy the launcher wrapper into the bundle directory.
  //    launcher.mjs is the .mcp.json entry point and is hand-written
  //    (not bundled by esbuild). It ensures node_modules/ exist before
  //    dynamically importing the real MCP server.
  await cp(
    resolve(ROOT, "plugin/launcher.mjs"),
    resolve(BUNDLE_DIR, "launcher.mjs"),
  );

  // 5. Generate plugin/package-lock.json for deterministic installs.
  //    This lockfile ships with the plugin so that the launcher's
  //    `npm install --omit=dev` resolves the exact same versions on
  //    every developer's machine. `--package-lock-only` does NOT
  //    create node_modules/, keeping the repo light.
  try {
    execFileSync("npm", [
      "install",
      "--package-lock-only",
      "--omit=dev",
    ], {
      cwd: resolve(ROOT, "plugin"),
      stdio: ["ignore", "inherit", "inherit"],
    });
  } catch (err) {
    process.stderr.write(
      `warning: could not generate plugin/package-lock.json: ${err.message}\n`,
    );
  }
  // 6. Sync to plugin cache — if Claude Code has cached a previous
  //    version of this plugin, update it in-place so the next session
  //    picks up the new build without a manual `/plugin install`.
  //    This is the developer fast path: `npm run build:plugin` is all
  //    you need after editing source. Normal users who install from
  //    the marketplace never hit this code.
  await syncToPluginCache(resolve(ROOT, "plugin"));
}

/**
 * Find every claude-crap version directory under the Claude Code
 * plugin cache and copy the freshly built plugin files into each one.
 * Skips `node_modules/` and `.claude-crap/` (runtime state that
 * belongs to the installed copy, not the source).
 */
async function syncToPluginCache(pluginDir) {
  const cacheBase = join(homedir(), ".claude", "plugins", "cache", "herz", "claude-crap");

  let versionDirs;
  try {
    versionDirs = await readdir(cacheBase);
  } catch {
    // No cache exists — nothing to sync (first install hasn't happened).
    return;
  }

  for (const version of versionDirs) {
    const cacheDir = join(cacheBase, version);
    const s = await stat(cacheDir).catch(() => null);
    if (!s?.isDirectory()) continue;

    try {
      // Copy every plugin file/directory EXCEPT node_modules and .claude-crap
      const entries = await readdir(pluginDir);
      for (const entry of entries) {
        if (entry === "node_modules" || entry === ".claude-crap") continue;
        const src = join(pluginDir, entry);
        const dst = join(cacheDir, entry);
        const srcStat = await stat(src);
        if (srcStat.isDirectory()) {
          await cp(src, dst, { recursive: true, force: true });
        } else {
          await cp(src, dst, { force: true });
        }
      }
      process.stderr.write(`  ✓ synced plugin cache: ${cacheDir}\n`);
    } catch (err) {
      process.stderr.write(`  ⚠ cache sync failed for ${cacheDir}: ${err.message}\n`);
    }
  }
}

main().catch((err) => {
  process.stderr.write(`bundle-plugin failed: ${err}\n`);
  process.exit(1);
});
